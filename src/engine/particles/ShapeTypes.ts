// ======================================================
// ShapeName：粒子/几何形状的语义枚举类型
// ======================================================
//
// 设计目的（IMPORTANT）：
// - 作为“共享类型层（Shared Types Layer）”存在
// - 用于 interaction / gesture / particles 等模块之间通信
// - 避免出现 interaction <-> particles 的循环依赖
//
// 架构角色说明：
// - ShapeName 不关心“形状如何实现”
// - 只描述“形状叫什么”（语义标识）
// - 真正的几何生成 / 渲染逻辑应位于 particles 模块内部
//
// 这种设计带来的好处：
// - interaction 层只需返回 ShapeName
// - particles 层根据 ShapeName 决定具体 Three.js / WebGL 实现
// - 后续新增形状时，不需要改动交互逻辑
//
// ======================================================

export type ShapeName =
/**
 * SPHERE：球形
 *
 * 典型用途：
 * - 默认形态
 * - 中性、无方向感的视觉对象
 */
    | "SPHERE"

    /**
     * CUBE：立方体
     *
     * 典型用途：
     * - 强结构感
     * - 可用于“模式切换 / 确认态”
     */
    | "CUBE"

    /**
     * TORUSKNOT：环面扭结
     *
     * 典型用途：
     * - 复杂、动态感强
     * - 常用于展示旋转、能量、活跃状态
     */
    | "TORUSKNOT"

    /**
     * WAVE：波形
     *
     * 语义说明：
     * - 并不一定是具体几何
     * - 更偏向“随时间/输入变化的形态”
     *
     * 典型用途：
     * - 音乐可视化
     * - 连续手势反馈
     */
    | "WAVE"

    /**
     * RING：环形
     *
     * 典型用途：
     * - 双手交互
     * - 围绕中心的控制反馈
     */
    | "RING"

    /**
     * HEART：心形
     *
     * 典型用途：
     * - 情绪化反馈
     * - 特殊手势触发（如点赞、确认）
     */
    | "HEART"

    /**
     * TEXT：文字形状
     *
     * 语义说明：
     * - 表示“以文字为形状源”
     * - 实际实现可能涉及字体、字形轮廓、点云采样等
     *
     * 注意（IMPORTANT）：
     * - ShapeName 仅标识“类型”
     * - 文字内容、字体等应通过额外参数传递
     */
    | "TEXT";
